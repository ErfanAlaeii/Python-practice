# Define two numbers and print the sum of them
num1 = 10
num2 = 20
sum1 = num1 + num2
print(sum1)

# Minus operation
diff = num1 - num2
print(diff)

# Multiplication operation
mul = num1 * num2
print(mul)

#  Division operation
div = num1 / num2
print(div)

# Modulus operation
mod = num1 % num2
print(mod)

# Exponential operation (power)
exp = num1 ** num2
print(exp)

# Floor division operation
floor = num1 // num2
print(floor)

# overwrite a variable
num1 = 10.5

# Type of variable
print(type(num1))
print(type(num2))

# Parse a number to integer
num3 = int(num1)
print(num3)

num4 = float(num3)
print(num4)

# if you want to parse a string to a number you can use the following, but you have to be sure that the string is a number
str1 = "10"
num5 = int(str1)
